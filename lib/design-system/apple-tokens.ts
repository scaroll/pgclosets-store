/**
 * Get responsive font size using clamp
 */
export function getResponsiveFontSize(
  minSize: number,
  maxSize: number,
  minViewport = 320,
  maxViewport = 1440
): string {
  const slope = (maxSize - minSize) / (maxViewport - minViewport);
  const yAxisIntersection = -minViewport * slope + minSize;

  return `clamp(${minSize}px, ${yAxisIntersection.toFixed(2)}px + ${(slope * 100).toFixed(2)}vw, ${maxSize}px)`;
}

/**
 * Convert hex color to rgba
 */
export function hexToRgba(hex: string, alpha: number): string {
  const r = parseInt(hex.slice(1, 3), 16);
  const g = parseInt(hex.slice(3, 5), 16);
  const b = parseInt(hex.slice(5, 7), 16);

  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

/**
 * Get elevation shadow based on level
 */
export function getElevation(level: 0 | 1 | 2 | 3 | 4 | 5): string {
  return appleShadows.elevation[level];
}

/**
 * Get spacing value
 */
export function getSpacing(value: keyof typeof appleSpacing): string {
  return appleSpacing[value];
}

